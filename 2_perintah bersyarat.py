# Operator relasional: >, <, ==, >=, <=, !=
# OPerator logika : and, or, not

print('Silahkan Maskkan nilai:')
nilai = float(input())
if nilai >=90 and nilai <=100:
    print('Nilai anda A')
elif nilai >=80 and nilai <=89:
    print('Nilai anda B')
elif nilai >=70 and nilai <=79:
    print('Nilai anda C')
else:
    print('Nilai dibawah KKM, Anda harus remidi')

    